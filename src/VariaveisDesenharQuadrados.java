
public class VariaveisDesenharQuadrados {

	private int distancia, nQuadrados;
	
	public VariaveisDesenharQuadrados() {
		setnQuadrados(1);
		setDistancia(15);
	}

	public int getDistancia() {
		return distancia;
	}

	public void setDistancia(int distancia) {
		this.distancia = distancia;
	}

	public int getnQuadrados() {
		return nQuadrados;
	}

	public void setnQuadrados(int nQuadrados) {
		this.nQuadrados = nQuadrados;
	}
	
}
